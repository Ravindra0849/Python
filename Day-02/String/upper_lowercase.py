
# we are  writing the  words in lower case we covert that into upper case using python

text = "python is awesome"
uppercase = text.upper()

print("Uppercase:", uppercase) # here output comes in upper case as "Uppercase: PYTHON IS AWESOME"


# we are  writing the  words in upper case we covert that into lower case using python

text = "PYTHON IS AWESOME"
lowercase = text.lower()

print("lowercase:", lowercase) # here output comes in upper case as "lowercase: python is awesome"

