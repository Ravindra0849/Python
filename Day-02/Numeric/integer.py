
# Integers are used for adding, multiplication, division, modulus, absolute value, and substraction of two numbers 

num1 = 10
num2 = 5

# addition

result1 = num1 + num2
print("addition:", result1)

# multiplication

result2 = num1 * num2
print("Multiplication:",result2)

# Division

result3 = num1 // num2
print("Division:", result3)

# Modulus

result4 = num1 % num2
print("Modulus (Remainder):", result4)

# Absolute value

result5 = abs(-9)
print("Absolute value:", result5)


# Output is 
# addition: 15
# Multiplication: 50
# Division: 2
# Modulus (Remainder): 0
# Absolute value: 9